void OLED_WrDat(byte data, byte revs)
{
	byte i=8;
	OLED_DC=1;
	asm("nop");
	OLED_SCL=0;
	asm("nop");
	if(revs) data=~data;
	while(i--)
	{
		if(data&0x80)
		{
			OLED_SDA=1;
		}
		else
		{
			OLED_SDA=0;
		}
		OLED_SCL=1;
		asm("nop");;

		OLED_SCL=0;;
		data<<=1;
	}
}
