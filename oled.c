	unsigned char i,j,k,l,m,fn=0;

	l  = data/10000;
	m= (data%10000)/1000;
	i = (data%1000)/100;
	j = (data%100)/10;
	k = data%10;
	if(data < 0)
	{
		OLED_P6x8Char('-');
		data = - data;
	}
