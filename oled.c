//功能描述：将一个float型数转换成2位整数部分带4位小数和符号的数据并进行显示
void OLED_PrintValueF(unsigned char x, unsigned char y, float data, unsigned char num)
{
	unsigned char l,m,i,j,k;  //wan
	unsigned char Integer_Len = 6; //整数位数

	if(data>0)
		data=data+0.00001;
	int Integer_Part = (int)data; //整数部分
	long int Float_Part = (long int)((data - (int)data)*100000); //取小数位后5位
	OLED_Set_Pos(x,y);
	//整数部分显示



	if(data<-0.00001)
		OLED_P6x8Char('-');

	if(Integer_Part < 0)Integer_Part = - Integer_Part;  //去掉整数部分负号
	l  = Integer_Part/10000;
	m= (Integer_Part%10000)/1000;
	i = (Integer_Part%1000)/100;
	j = (Integer_Part%100)/10;
	k = Integer_Part%10;




	if (l != 0)  //五位
	{
		Integer_Len = 5;
		OLED_P6x8Char(l+48);
		OLED_P6x8Char(m+48);
		OLED_P6x8Char(i+48);
		OLED_P6x8Char(j+48);
		OLED_P6x8Char(k+48);
	}
	else if(m != 0) //四位
	{
		Integer_Len = 4;
		OLED_P6x8Char(m+48);
		OLED_P6x8Char(i+48);
		OLED_P6x8Char(j+48);
		OLED_P6x8Char(k+48);
	}
	else if(i != 0) //三位
	{
		Integer_Len = 3;
		OLED_P6x8Char(i+48);
		OLED_P6x8Char(j+48);
		OLED_P6x8Char(k+48);
	}
	else if(j != 0) //两位
	{
		Integer_Len = 2;
		OLED_P6x8Char(j+48);
		OLED_P6x8Char(k+48);
	}
	else
	{
		Integer_Len = 1;
		OLED_P6x8Char(k+48);
	}
	if(Float_Part < 0)Float_Part = -Float_Part;
	switch(num-Integer_Len)
	{
	case 0:
		OLED_P6x8Char(' ');
		break;
	case 1:
		OLED_PrintValueFP((unsigned int)(Float_Part /10000),num-Integer_Len);
		break;
	case 2:
		OLED_PrintValueFP((unsigned int)(Float_Part /1000),num-Integer_Len);
		break;
	case 3:
		OLED_PrintValueFP((unsigned int)(Float_Part /100),num-Integer_Len);
		break;
	case 4:
		OLED_PrintValueFP((unsigned int)(Float_Part/10),num-Integer_Len);
		break;
	}
}
