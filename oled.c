void OLED_P6x8Char(unsigned char ch)
{
	unsigned char c=0,i=0,rs=0;
	c =ch-32;
	if(reverse&&c) rs=1;
	for(i=0; i<6; i++)
	{
		OLED_WrDat(F6x8[c][i],rs);
	}
}



